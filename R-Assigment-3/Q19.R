# Define two strings
string1 <- "Apple"
string2 <- "banana"

# Compare strings alphabetically using relational operators
string1 < string2   # TRUE
string1 > string2   # FALSE
string1 == string2  # FALSE

# Explanation:

# Uppercase letters (A-Z) have lower values than lowercase letters (a-z).
# Therefore, all uppercase letters come BEFORE lowercase letters.

